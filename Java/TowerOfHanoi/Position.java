package towerofhanoi;

// -------------------------------------------------------------------------
/**
 * Enumerator that defines the spaces on the Tower of Hanoi
 * 
 * @author Connor Stratton
 * @version Oct 8, 2024
 */
public enum Position
{
    /**
     * Represents left peg
     */
    LEFT,
    /**
     * Represents center peg
     */
    CENTER,
    /**
     * Represents right peg
     */
    RIGHT,
    /**
     * Represents default case
     */
    DEFAULT;
}
